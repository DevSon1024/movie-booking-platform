import mongoose from 'mongoose';

const reviewSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  movie: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Movie',
    required: true,
  },
  rating: {
    type: Number,
    required: true,
    min: 1,
    max: 5,
  },
  comment: {
    type: String,
    required: true,
  },
  isVerifiedWatcher: {
    type: Boolean,
    default: false, // Will be set to true if logic passes
  }
}, {
  timestamps: true,
});

const Review = mongoose.model('Review', reviewSchema);
export default Review;